  <h2 style="text-align: center; font-size: 45px;">Our Concept</h2>
  <h4 style="text-align: center;">The rise of experiential travel</h4>
  <br>
  <p style="text-align: center;">
  <i>"Atithi Devo Bhava" </i><br><br>
  Meaning: Guest is God<br><br>
  Tourism has developed to a level where it has become a major industry, a major force for social change and a major power for good, for evil.
  Focusing on the evil side, many of the tourists are misguided by people living in the local areas. Lack of clean products, overpriced items and unsafe environment 
  are another set of problems faced by them. So, we decided to took a step further, defend and prove our saying - Atithi Devo Bhava by our concept of Khaatirdaari!
  We rely on local experts to provide travelers the most authentic travel experiences. 
These local experts are aiming to be defined as local friends. A friend that can adapt the route according to the tourists's desires and needs.
  </p>



<br>
<br>

 <h2 style="text-align: center;">Tailor-made experiences</h2><br>
